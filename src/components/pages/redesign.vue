<p><b>Rediscovering a New Dimension of Luxury</b></p>
<p>As we move forward to a new normal, Mezzo Hotel’s top priority is the health, safety, and security of our guests, associates, and partners. The recent pandemic, COVID-19 has completely changed the way we live, and we are adjusting our daily operations to fit within the new normal.</p>
<p>Here at Mezzo Hotel, we have enhanced our cleaning protocols: surfaces are thoroughly disinfected and misting and cleaning are conducted at an increased frequency.</p>
<p><b>Safe Together</b></p>
<p>All guests and associates must strictly adhere to these guidelines:</p>
<ol>
  <li>Face masks should be worn at all times.</li>
  <li>Before entering the Hotel, footwear and hands are sanitized, and temperature checks are performed at all entry points.</li>
  <li>Guests or associates with a temperature above 37.5°C or showing any symptoms of COVID-19 shall be isolated and assisted to the nearest hospital.</li>
  <li>Proper hand washing should be observed. Alcohol pumps are made available around the Hotel to encourage every guest and associate to sanitize regularly.</li>
  <li>Maintain a safe distance of at least 6 feet (about 2 arms’ length) from each other.</li>
  <li>Follow Social Distancing Stickers in the elevator where only four (4) can ride at a time.</li>
</ol>
<p><b>Guest Experience</b></p>
<p><b>Safety Measures upon Arrival and Departure</b></p>
<ol style="list-style-type:square;">
  <li>An acrylic barrier is installed at our Front Desk.</li>
  <li>Digital Health Declaration Form via QR code has been available to minimize physical contact.</li>
  <li>Upon checking in, a medical certificate must be presented for non-Cebu residents.</li>
  <li>Clean and disinfected keycards are provided upon check-in.</li>
  <li>Various cashless payment methods are accepted.</li>
  <li>Luggage assistance will be done only as appropriate.</li>
</ol>   
<p><b>Safe Stay</b></p>
<ol style="list-style-type:square;">
  <li>Enhanced cleaning and sanitizing protocols when cleaning guest rooms, with particular attention to frequently touched items.</li>
  <li>Each guest room is provided with a sanitation kit.</li>
  <li>All air-conditioning units are running on fresh air mode for constant airflow.</li>
  <li>Individually packaged breakfast food are offered.</li>
  <li>All room types are offered and strictly follow physical distancing at all times.</li>
  <li>Strictly no outside guests are allowed in the guest rooms.</li>
  <li>Designated isolation room for persons who feel unwell.</li>
  <li>Make-up room will only be available upon request. To minimize direct contact, we request that our guest to stay outside the room during cleaning service.</li>
</ol>   
<p><b>Delivering Confidence through Food Safety</b></p>
<ol style="list-style-type:square;">
  <li>Strict adherence to food safety procedures and proper personal hygiene.</li>
  <li>Contactless menu via QR code has been made available to minimize physical contact.</li>
  <li>Food, beverage, and tableware are stored and covered properly.</li>
  <li>All utensils and equipment are properly washed, cleaned, and sanitized prior to its use.</li>
  <li>Regular sanitation of high-contact areas and surfaces (e.g., door handles, common tables, etc.)</li>
  <li>Proper disposal of disposable utensils, containers, and food items.</li>
</ol>  
<p><b>Banquet and Events</b></p>
<ol style="list-style-type:square;">
  <li>Adjusted function room capacity to adhere to physical distancing protocols.</li>
  <li>Seating arrangement adjusted to provide adequate spacing between seats.</li>
  <li>Thorough disinfection of all function spaces and event paraphernalia before and after an event.</li>
  <li>Buffet-style food service is suspended and replaced by a plated or family-style arrangement.</li>
  <li>All participants' contact information will be saved and should be kept on file and will only be given to local public health officials if a suspected infection arises.</li>
</ol>
<p><b>Recreational Areas</b></p>
<p>Our swimming pool is open from 7 AM to 6 PM and our gym is open from 6 AM to 6 PM.</p>
<p><b>Health Concerns</b></p>
<p>All Associates of Mezzo Hotel are presented with clear instructions on how to respond and report all presumed cases of COVID-19 on-property, and we are ready to provide support to our guests. Associates are instructed to stay at home if they are not feeling well.</p>
